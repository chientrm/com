<h2>Data privacy</h2>

<p>
  YouWave Android doesn't collect any of your personal data. Personal data are
  securely handled by Google Firebase.
</p>

<p>Anonymous user can use most of the features provided by YouWave.</p>

<p>
  In order to upload audio, user need to register a unique `handle` with YouWave
  which is the only personal information that is exposed to the internet.
</p>

<p>
  YouWave only stores the processed version of your uploaded assets.
  Intermediate resources are removed once the processing is done.
</p>

<p>
  Users can choose to help us with the app Crashlytics and diagnosis by sending
  app info and performance. The data sent is handled by Firebase Crashlytics and
  Performance.
</p>

<p>
  We intentionally collect your app interactions so that we can make the app
  more eligible and usable which would help improve user experience.
</p>

<p>
  We intentionally collect your search history so that we can personalize your
  user experience.
</p>

<p>
  Users can always review, enable or disable app interactions and search history
  data collection.
</p>

<p>
  By removing your audio from the app, your audio file and related information
  are no longer available on our server.
</p>

<p>
  By choosing to delete data, all personal data and assets are completely
  removed from our servers.
</p>

<p>
  All private information is not exposed or shared with any other users or
  third-party organization. If you find yourself or other people's information
  exposed, please contact us using the `Contact information` provided at the
  bottom of this document so that we can fix the issue.
</p>

